export interface CategoryData {
  iconKey: string;
  title: string;
  desc: string;
  href?: string;
}

export default function CategoryCard({ cat }: { cat: CategoryData }) {
  const inner = (
    <div
      className="relative rounded-xl p-6 border transition-all duration-300 h-full"
      style={{
        background: "var(--surface)",
        borderColor: "var(--border)",
        cursor: cat.href ? "pointer" : "default",
      }}
      onMouseEnter={(e) => {
        (e.currentTarget as HTMLDivElement).style.borderColor = "var(--accent)";
        (e.currentTarget as HTMLDivElement).style.background = "var(--accent-dim)";
      }}
      onMouseLeave={(e) => {
        (e.currentTarget as HTMLDivElement).style.borderColor = "var(--border)";
        (e.currentTarget as HTMLDivElement).style.background = "var(--surface)";
      }}
    >
      <span
        className="absolute top-4 right-4 font-mono text-[10px] px-2 py-0.5 rounded-full border"
        style={
          cat.href
            ? { borderColor: "var(--accent)", color: "var(--accent)", background: "var(--accent-dim)" }
            : { borderColor: "var(--border)", color: "var(--text-secondary)", background: "var(--bg)" }
        }
      >
        {cat.href ? "Verfügbar →" : "Coming soon"}
      </span>

      <div style={{ color: "var(--accent)" }} className="mb-4">
        {icons[cat.iconKey]}
      </div>
      <h3 className="font-bold text-base mb-2">{cat.title}</h3>
      <p className="text-sm leading-relaxed" style={{ color: "var(--text-secondary)" }}>
        {cat.desc}
      </p>
    </div>
  );

  if (cat.href) {
    return <Link href={cat.href} className="block">{inner}</Link>;
  }
  return inner;
}
